package practica2y3;

public class Ruta {
    int paso1;
    int paso2;
    int paso3;

    public Ruta(int paso1, int paso2, int paso3) {
        this.paso1 = paso1;
        this.paso2 = paso2;
        this.paso3 = paso3;
    }
    public int getPaso1() {
        return paso1;
    }

    public void setPaso1(int paso1) {
        this.paso1 = paso1;
    }

    public int getPaso2() {
        return paso2;
    }

    public void setPaso2(int paso2) {
        this.paso2 = paso2;
    }

    public int getPaso3() {
        return paso3;
    }

    public void setPaso3(int paso3) {
        this.paso3 = paso3;
    }
}
